    private void btValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderActionPerformed
        try {
            int reponse = JOptionPane.showConfirmDialog(this, "Valider la modification ?", "Validation", JOptionPane.YES_NO_OPTION);
            if (reponse == JOptionPane.YES_OPTION) {
                String idPhoto = txtNomPhoto.getText() + extension;
                if (txtNomPhoto.getText().isEmpty()) {
                    throw new Exception("Champ nom photo vide");
                }
                oldIdPhoto = laPhoto.getIdPhoto();
                laPhoto.setIdPhoto(idPhoto);

                String lieuPhoto = txtLieuPhoto.getText();
                if (lieuPhoto.isEmpty()) {
                    throw new Exception("Champ lieu vide");
                }
                laPhoto.setLieuPhoto(lieuPhoto);

                int anneePhoto = Integer.parseInt(txtAnneePhoto.getText());
                if (txtAnneePhoto.getText().isEmpty()) {
                    throw new Exception("Champ annee vide");
                }
                laPhoto.setAnneePhoto(anneePhoto);

                modal = true;
                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erreur modification photo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btValiderActionPerformed

